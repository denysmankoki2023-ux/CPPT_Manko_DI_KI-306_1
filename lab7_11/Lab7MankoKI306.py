"""
Лабораторна робота №1.
Модуль генерує зубчастий список, який містить лише заштриховану,
задану варіантом, область квадратної матриці (варіант 11).

Програма:
 • запитує розмір квадратної матриці;
 • запитує символ-заповнювач;
 • будує зубчастий список потрібної форми;
 • виводить масив на екран
"""

def main():
    """
    Головна функція програми.
    """
    # Введення даних та перевірка
    try:
        size_input = input("Введіть розмір матриці (парне число, наприклад 4, 6, 8): ")
        
        if not size_input.isdigit() or int(size_input) <= 0 or int(size_input) % 2 != 0:
            print("Помилка: Необхідно ввести додатне ПАРНЕ ціле число.")
            return
        
        n = int(size_input)
        
        filler = input("Введіть символ-заповнювач: ")
        
        # Перевірка на кількість символів
        if len(filler) != 1:
            print("Помилка: Необхідно ввести рівно один символ.")
            return

    except Exception as e:
        print(f"Виникла помилка: {e}")
        return

    # Генерація зубчастого списку
    jagged_list = []
    half = n // 2 

    for i in range(n):
        # Якщо i < half (верхня половина) -> заповнюємо праву частину
        # Якщо i >= half (нижня половина) -> заповнюємо ліву частину
        # Для симетричної матриці N/2 дає рівні половини
        
        count = n - half if i < half else half
        row = [filler] * count
        jagged_list.append(row)

    # Виведення результату на екран
    print("\nСформована матриця:")
    
    for i in range(n):
        line_output = ""
        
        # Якщо це верхня половина (правий верхній кут), 
        # додаємо відступи зліва
        if i < half:
            line_output += "  " * half
        
        # Додаємо елементи з масиву
        for char in jagged_list[i]:
            line_output += char + " "
            
        print(line_output)

if __name__ == "__main__":
    main()